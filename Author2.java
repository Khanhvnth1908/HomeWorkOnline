package Jp_04;

/**
 *
 * @author User
 */
public class Author2 {
    private String name;
    private String email;
    private char gender;

    public Author2(String name, String email, char gender) {
        this.name = name;
        this.email = email;
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public char getGender() {
        return gender;
    }

    @Override
    public String toString() {
        return "Author2{" + "name=" + name + ", email=" + email + ", gender=" + gender + '}';
    }
    
    
}
